package march2;

public class ForLooping {

	public static void main(String[] args) {
		for(int i=1; i<=5; i++) {
			System.out.println(i+ "My Name is Deepa");
		}
		System.out.println("=================================================");
		for (int i=5; i>=1; i--) {
			System.out.println(i+ "My name is Deepu");
		}
		System.out.println("=================================================");
	}
}
