package march1;

public class IncandDec {

	public static void main(String[] args) {
		int i=100;
		i++;
		System.out.println("increment with one time :: "+i);//101
		int j= 200;
		j+=2;
		System.out.println("increment with 2 times :: "+j);//202
		int x=300;
		x--;
		System.out.println("decrement by one time ::"+x);//299
		int y=400;
		y-=50;
		System.out.println("Decrement by 50 times ::"+y);//350

	}

}
