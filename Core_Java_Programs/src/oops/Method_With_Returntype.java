package oops;

public class Method_With_Returntype {

	public static void main(String[] args) {
		// Methods with Return type
		run();
	}
	public static int run() {
		System.out.println("In Run Method");
		return 0;
	}

}
/*If we are defining a method with return type it is mandatory to use return keyword inside the Method
 * Return keyword is use to exit from the method with or without taking a value
 * 
 * Note : In a Method we cannot have more than one return statement
 * Return keyword is not for printing it is only use for existing from the Method.
 */
		