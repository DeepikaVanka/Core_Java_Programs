package basic_programs;

public class Arthematic_Operators {

	public static void main(String[] args) {
		
		// Adding of two values
		int a=5; 
		int b=10;
		int c= a+b;
		System.out.println("The Sum of A and B is :: "+c);
		
		//Subtraction of two values
		int d = b-a;
		System.out.println("The Subtraction of B and A is :: "+ d);
		
		//Multiplication of two values
		int e = a*b;
		System.out.println("The Multiplication of A and B is :: "+ e);

		//Division of two values
		int f= b/a;
		System.out.println("The Division of A and B is :: "+ f);
		
		//Modular of two values
		int g=b%a;
		System.out.println("The Modular value of A and B is :: "+g);
		
	}

}
